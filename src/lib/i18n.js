export function t(key) {
  return strings[currentLang]?.[key] || strings.en[key] || key
}

export function getLang() {
  return currentLang
}

export function setLang(code) {
  if (strings[code]) {
    currentLang = code
    localStorage.setItem('lola_lang', code)
  }
}
